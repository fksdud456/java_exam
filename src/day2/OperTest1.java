package day2;
public class OperTest1 {
	public static void main(String[] args) {
		int su=10;
		System.out.println(++su);  // 11
		System.out.println(++su);  // 12
		System.out.println(su++);  // 12
		System.out.println(su);      // 13
		System.out.println(++su);  // 14
		System.out.println(su++);  // 14
		System.out.println(su);      // 15
	}
}



